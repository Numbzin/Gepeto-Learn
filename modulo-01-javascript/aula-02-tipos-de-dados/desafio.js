// Crie 5 variáveis com os tipos: string, number, boolean, null e undefined
// Use typeof para imprimir o tipo de cada uma

let string = "Alo";
let number = 1;
let boolean = true;
let nullValue = null;
let undefinedValue;

console.log(typeof string); // string
console.log(typeof number); // number
console.log(typeof boolean); // boolean
console.log(typeof nullValue); // object (isso é um bug do JavaScript)
console.log(typeof undefinedValue); // undefined
