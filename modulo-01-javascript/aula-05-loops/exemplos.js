// for
for (let i = 1; i <= 5; i++) {
  console.log("Número:", i)
}

// while
let j = 1
while (j <= 5) {
  console.log("Contador:", j)
  j++
}

// do...while
let k = 1
do {
  console.log("Valor:", k)
  k++
} while (k <= 5)

// for...of
const nomes = ["João", "Maria", "José"]
for (const nome of nomes) {
  console.log("Nome:", nome)
}
